#include <stdio.h>
#include <stdlib.h>

double F(double a, double b)
{
    return a+b;
}

double G(double a, double b, double c)
{
    return a+b-c;
}

double H(double a, double b)
{
    return a/b;
}

double I(double a)
{
    return a;
}

